/**
 * MathService
 *
 * @author chuangcius
 * @date 2022.11.30
 */
class MathService {

    fun multiply(firstNumber: Int, secondNumber: Int): Int {
        return firstNumber * secondNumber
    }

    fun subtract(firstNumber: Int, secondNumber: Int): Int {
        return firstNumber - secondNumber
    }
}
